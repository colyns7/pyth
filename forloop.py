#for loop-for itreating through a sequence

#looping through a list
fruits = ["apple","banana","orange"]
for x in fruits:
    print(x)
#looping through a string
for x in "HELLO":
    print(x)
#looping through a range 
for z in range(2,6):
    print(z)
for b in range(9):
    print(b)
#step
for c in range(20,10,-2):
    print(c)