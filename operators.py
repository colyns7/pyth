#arithemetic operators + - * / %
#additionb +
x = 20
y = 30
print(f"The sum of {x} and {y} is {x+y}")
#subtraction -
print(f"The difference of {y} and {x} is {y - x}")
#multiplication *
print(f"The product of {x} and {y} is {x * y}")
#division /
print(f"The division of {y} by {x} is {y / x}")
#modulus % the remainder after division
print(f"The modulus of {y} and {x} is {y % x}")
#power **
print(f"{x} to the power of 2 is {x ** 3}")
#comparison operators > < >= <= == !=
#greater than >
print(f"Is {y} greater than {x}? {y > x}")

#less than <
print(f"Is {x} less than {y}? {x < y}")
#greater than or equal to >=
print(f"Is {y} greater than or equal to {x}? {y >= x}")
#less than or equal to <=
print(f"Is {x} less than or equal to {y}? {x <= y}")
#equal to ==
print(f"Is {x} equal to {y}? {x == y}")
#not equal to !=
print(f"Is {x} not equal to {y}? {x != y}")
#logical operators and,or and not
#and-returns if both statements are true
z=10
print(z > 20 and z < 5)
print(z > 20 and z < 5)
#or-returns true if one of the statements is true
print(z > 20 or z < 5)
#not-returns the reverse
print(not(z > 5))
